package calculatorgui;

/**
 *
 * @author anda
 */
public class operations {
    int ansa ;
    public int add(int a, int b){
        ansa = a + b;
        return ansa;
    }
    public int subtract(int a, int b){
        ansa = a - b;
        return ansa;
    }
    public int division(int a, int b){
        ansa = a / b;
        return ansa;
    }
    public int multiply(int a, int b){
        ansa = a * b;
        return ansa;
    }
    
}
